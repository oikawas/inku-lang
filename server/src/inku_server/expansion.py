def expand_input(text: str) -> str:
    """テキスト内の非 Saijiki 語を検出し、展開ヒントを付加した文字列を返す。

    マッチ戦略: 長いキーを優先してマッチし、マッチ済み範囲を消費済みとして
    短いキーの衝突を防ぐ (「水平線」が「水」より先にヒットする)。
    変換候補が見つからなければ元のテキストをそのまま返す。
    """
    found: dict[str, str] = {}
    consumed: list[tuple[int, int]] = []  # (start, end) の消費済み範囲

    for key in sorted(IMAGERY_EXPANSION, key=len, reverse=True):
        idx = 0
        while True:
            pos = text.find(key, idx)
            if pos == -1:
                break
            end = pos + len(key)
            # 消費済み範囲と重複していなければマッチ確定
            if not any(s < end and pos < e for s, e in consumed):
                found[key] = IMAGERY_EXPANSION[key]
                consumed.append((pos, end))
                break
            idx = pos + 1

    if not found:
        return text

    hints = "\n".join(f"  {k} → {v}" for k, v in found.items())
    return f"{text}\n\n[語彙展開ヒント — 非 Saijiki 語の変換候補]\n{hints}"
